package me.kdav.cats;


/**
 * ImageX
 */
public class ImageX {

    private String id = "";
    private String url = "";

    /**
     * Get id.
     *
     * @return id as String.
     */
    public String getId() {
        return id;
    }

    /**
     * Set id.
     *
     * @param id the value to set.
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * Get url.
     *
     * @return url as String.
     */
    public String getUrl() {
        return url;
    }

    /**
     * Set url.
     *
     * @param url the value to set.
     */
    public void setUrl(String url) {
        this.url = url;
    }
}
